            <b>อาณาเขต</b>
            <p style="text-indent:0.5in">
                หมู่บ้านวังตูม หมู่ที่ ๑ ตำบลเขาพระ อำเภอเมืองนครนายก จังหวัดนครนายก อยู่ห่างจากที่ว่าการอำเภอเมืองนครนยก ประมาณ ๕ กิโลเมตร มีอาณาเขตดังนี้
            </p>
            <ul>
