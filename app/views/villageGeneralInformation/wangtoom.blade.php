            <b>ประวัติหมู่บ้าน</b>
            <p style="text-indent:0.5in"> 
                หมู่บ้านวังตูม หมู่ที่ ๑ ตำบลเขาพระ อำเภอเมืองนครนายก แต่ดั้งเดิมนั้นเมื่อประมาณ ๑๐๐ กว่าปีที่ผ่านมา คนเฒ่าคนแก่ได้เล่าขานสืบต่อกันมาเมื่อก่อนนั้นพื้นที่บริเวณนี้เป็นลักษณะ สภาพพื้นที่มีแหล่งน้ำเป็นวังน้ำวน ล้อมรอบด้วยต้นมะตูม เวลาฤดูฝนจะมีน้ำท่วมรอบๆ หมู่บ้านซึ่งมีต้นมะตูมมากมายอยู่ในวังน้ำ จึงได้ตั้งชื่อหมู่บ้านว่า “หมู่บ้านวังตูม”
            </p>
            <b>สภาพทางภูมิศาสตร์</b>
            <p style="text-indent:0.5in">
                สภาพทางภูมิศาสตร์ของหมู่บ้านอยู่ทางทิศใต้ของตำบลเขาพระ มีสภาพเป็นที่ราบลุ่มสภาพบ้านเรือนส่วนใหญ่จะปลูกห่างกัน พื้นที่ของหมู่บ้านเหมาะสมกับการทำนา พื้นที่ทั้งหมดจำนวน ๒,๐๐๐ ไร่ พื้นที่การเกษตร ๑,๖๐๐ ไร่ ดินส่วนใหญ่มีปัญหาคือดินเปรี้ยว
            </p>
            <b>จำนวนครัวเรือนและประชากร</b>
